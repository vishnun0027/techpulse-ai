CREATE TRIGGER update_tenant_profiles_updated_at BEFORE UPDATE ON tenant_profiles
    FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();
CREATE TRIGGER update_rss_sources_updated_at BEFORE UPDATE ON rss_sources
    FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();
CREATE TRIGGER update_article_events_updated_at BEFORE UPDATE ON article_events
    FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();
CREATE TRIGGER update_app_config_updated_at BEFORE UPDATE ON app_config
    FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();

-- 6. RPC FUNCTIONS

-- Semantic search per user
CREATE OR REPLACE FUNCTION match_articles(
  query_embedding  vector(768),
  match_threshold  float,
  match_count      int,
  p_user_id        uuid
) RETURNS TABLE (id uuid, title text, summary text, why_it_matters text, published_at timestamptz, similarity float)
LANGUAGE sql STABLE AS $$
  SELECT id, title, summary, why_it_matters, published_at, 1 - (embedding <=> query_embedding) AS similarity
  FROM articles WHERE user_id = p_user_id AND embedding IS NOT NULL AND 1 - (embedding <=> query_embedding) > match_threshold
  ORDER BY similarity DESC LIMIT match_count;
$$;

-- Near-duplicate check
CREATE OR REPLACE FUNCTION is_near_duplicate(query_embedding vector(768), dup_threshold float, p_user_id uuid)
RETURNS boolean LANGUAGE sql STABLE AS $$
  SELECT EXISTS (SELECT 1 FROM articles WHERE user_id = p_user_id AND embedding IS NOT NULL AND 1 - (embedding <=> query_embedding) >= dup_threshold);
$$;

-- Atomic increment for source ingestion tracking
CREATE OR REPLACE FUNCTION increment_source_ingestion(p_source_id bigint, p_user_id uuid)
RETURNS void LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
    INSERT INTO source_health (source_id, user_id, articles_ingested)
    VALUES (p_source_id, p_user_id, 1)
    ON CONFLICT (source_id, user_id)
    DO UPDATE SET articles_ingested = source_health.articles_ingested + 1, last_ingested_at = now();
END;
$$;

-- Match thematic clusters
CREATE OR REPLACE FUNCTION match_events_by_centroid(query_embedding vector(768), threshold float, p_user_id uuid)
RETURNS TABLE (id uuid, article_count int, similarity float)
LANGUAGE sql STABLE AS $$
  SELECT id, article_count, 1 - (centroid_embedding <=> query_embedding) AS similarity
  FROM article_events WHERE user_id = p_user_id AND centroid_embedding IS NOT NULL AND 1 - (centroid_embedding <=> query_embedding) >= threshold
  ORDER BY similarity DESC LIMIT 5;
$$;

